package org.example.Reflection;

public class Example {

    private final static Integer num1 = 100;
    private final static Integer num2 = 100;
    private final static String name = "Pear1y";
    private final static StringBuilder sbstring = new StringBuilder("sbstring");

    public static void printNum1() {
        System.out.println(num1);
    }

    public static void printNum2() {
        System.out.println(num2);
    }

    public static void printName() {
        System.out.println(name);
    }

    public static void printsbstring() {
        System.out.println(sbstring);
    }

}
